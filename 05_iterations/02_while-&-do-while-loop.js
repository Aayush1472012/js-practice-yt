// while loop
let i = 0;
while(i < 10){
    console.log(i);
    i = i+2;
}

let marvel_heros = ["Dr.Strange", "Spiderman", "Ironman", "Hulk"];
let arr_index = 0;
while(arr_index < marvel_heros.length){
    console.log(marvel_heros[arr_index]);
    arr_index++;
}



// do-while loop
let score = 10;

do{
    console.log(score);
    score++;
}while(score < 10);